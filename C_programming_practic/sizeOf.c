#include<stdio.h>
int main()
{
    int i;
    float f;
    double d;
    char c;
    printf("Size of int = %d\n",sizeof(i)); //4 bites
    printf("Size of float = %d\n",sizeof(f)); //4bites
    printf("Size of double = %d\n",sizeof(d)); //8bits
    printf("Size of char = %d\n",sizeof(c)); //1bits
}
